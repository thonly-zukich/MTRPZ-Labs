# –Ü–º–ø–æ—Ä—Ç—É—î–º–æ –Ω–µ–æ–±—Ö—ñ–¥–Ω—ñ –±—ñ–±–ª—ñ–æ—Ç–µ–∫–∏
import math  # –ú—ñ—Å—Ç–∏—Ç—å –º–∞—Ç–µ–º–∞—Ç–∏—á–Ω—ñ —Ñ—É–Ω–∫—Ü—ñ—ó, –∑–æ–∫—Ä–µ–º–∞ sqrt() –¥–ª—è –æ–±—á–∏—Å–ª–µ–Ω–Ω—è –∫–æ—Ä–µ–Ω—è –¥–∏—Å–∫—Ä–∏–º—ñ–Ω–∞–Ω—Ç–∞
import sys   # –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É—î—Ç—å—Å—è –¥–ª—è –æ–±—Ä–æ–±–∫–∏ –∞—Ä–≥—É–º–µ–Ω—Ç—ñ–≤ –∫–æ–º–∞–Ω–¥–Ω–æ–≥–æ —Ä—è–¥–∫–∞

# –§—É–Ω–∫—Ü—ñ—è –¥–ª—è —Ä–æ–∑–≤‚Äô—è–∑–∫—É –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–≥–æ —Ä—ñ–≤–Ω—è–Ω–Ω—è
def solve_funny_math(alpha, beta, gamma):
    """
    –†–æ–∑–≤'—è–∑—É—î –∫–≤–∞–¥—Ä–∞—Ç–Ω–µ —Ä—ñ–≤–Ω—è–Ω–Ω—è Œ±x¬≤ + Œ≤x + Œ≥ = 0

    –ü–∞—Ä–∞–º–µ—Ç—Ä–∏:
    alpha (float): –ö–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç –ø—Ä–∏ x¬≤ (–Ω–µ –º–æ–∂–µ –±—É—Ç–∏ –Ω—É–ª–µ–º)
    beta (float): –ö–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç –ø—Ä–∏ x
    gamma (float): –í—ñ–ª—å–Ω–∏–π —á–ª–µ–Ω

    –ü–æ–≤–µ—Ä—Ç–∞—î:
    tuple: –ü–∞—Ä–∞ –∫–æ—Ä–µ–Ω—ñ–≤ (–∞–±–æ –æ–¥–∏–Ω –∫–æ—Ä—ñ–Ω—å, –∞–±–æ –ø–æ—Ä–æ–∂–Ω—ñ–π –∫–æ—Ä—Ç–µ–∂)
    """

    if alpha == 0:
        raise ValueError("Oops! Alpha (a) cannot be zero.")  # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ, —â–æ–± alpha ‚â† 0

    # –û–±—á–∏—Å–ª—é—î–º–æ –¥–∏—Å–∫—Ä–∏–º—ñ–Ω–∞–Ω—Ç
    discriminant = beta ** 2 - 4 * alpha * gamma  # –§–æ—Ä–º—É–ª–∞ –¥–∏—Å–∫—Ä–∏–º—ñ–Ω–∞–Ω—Ç–∞: D = b¬≤ - 4ac

    # –í–∏–∑–Ω–∞—á–∞—î–º–æ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –∫–æ—Ä–µ–Ω—ñ–≤
    if discriminant > 0:
        # –î–≤–∞ –¥—ñ–π—Å–Ω–∏—Ö –∫–æ—Ä–µ–Ω—ñ
        root1 = (-beta + math.sqrt(discriminant)) / (2 * alpha)
        root2 = (-beta - math.sqrt(discriminant)) / (2 * alpha)
        return (root1, root2)
    elif discriminant == 0:
        # –û–¥–∏–Ω –∫–æ—Ä—ñ–Ω—å (–∫—Ä–∞—Ç–Ω–∏–π)
        single_root = -beta / (2 * alpha)
        return (single_root,)
    else:
        # –ù–µ–º–∞—î –¥—ñ–π—Å–Ω–∏—Ö –∫–æ—Ä–µ–Ω—ñ–≤
        return ()

# –§—É–Ω–∫—Ü—ñ—è –¥–ª—è –∑–∞–ø—É—Å–∫—É –≤ —ñ–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω–æ–º—É —Ä–µ–∂–∏–º—ñ (–∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤–≤–æ–¥–∏—Ç—å –∑–Ω–∞—á–µ–Ω–Ω—è –≤—Ä—É—á–Ω—É)
def fun_interactive_mode():
    """
    –Ü–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω–∏–π —Ä–µ–∂–∏–º: –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á –≤–≤–æ–¥–∏—Ç—å –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç–∏ —Ä—ñ–≤–Ω—è–Ω–Ω—è
    """

    print("üéâ Super Fun Quadratic Equation Solver üéâ")
    print("Equation format: Œ±x^2 + Œ≤x + Œ≥ = 0")

    while True:
        try:
            # –ó–∞–ø–∏—Ç—É—î–º–æ —É –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç–∏
            alpha = float(input("Enter Œ± (a) = "))  # –í–≤–µ–¥–µ–Ω–Ω—è –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç–∞ alpha
            if alpha == 0:
                print("üö® Oops! Alpha cannot be zero, try again!")  # –ó–∞–±–æ—Ä–æ–Ω—è—î–º–æ –Ω—É–ª—å–æ–≤–µ –∑–Ω–∞—á–µ–Ω–Ω—è
                continue

            beta = float(input("Enter Œ≤ (b) = "))  # –í–≤–µ–¥–µ–Ω–Ω—è –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç–∞ beta
            gamma = float(input("Enter Œ≥ (c) = "))  # –í–≤–µ–¥–µ–Ω–Ω—è –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç–∞ gamma

            # –í–∏–∫–ª–∏–∫–∞—î–º–æ —Ñ—É–Ω–∫—Ü—ñ—é —Ä–æ–∑–≤‚Äô—è–∑–∫—É
            roots = solve_funny_math(alpha, beta, gamma)

            # –í–∏–≤–æ–¥–∏–º–æ —Ä—ñ–≤–Ω—è–Ω–Ω—è —É –∑—Ä–æ–∑—É–º—ñ–ª–æ–º—É —Ñ–æ—Ä–º–∞—Ç—ñ
            print(f"Equation is: ({alpha}) x^2 + ({beta}) x + ({gamma}) = 0")

            # –í–∏–≤–æ–¥–∏–º–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∏
            if len(roots) == 2:
                print("üéâ There are 2 fantastic roots!")
                print(f"‚ú® Root 1: {roots[0]:.6g}")
                print(f"‚ú® Root 2: {roots[1]:.6g}")
            elif len(roots) == 1:
                print("‚≠ê Just 1 unique root!")
                print(f"ü¶Ñ Root: {roots[0]:.6g}")
            else:
                print("üí® No real roots this time, sorry!")

            break  # –í–∏—Ö–æ–¥–∏–º–æ –ø—ñ—Å–ª—è —É—Å–ø—ñ—à–Ω–æ–≥–æ —Ä–æ–∑—Ä–∞—Ö—É–Ω–∫—É
        except ValueError:
            print("üö® Whoops! That's not a valid number. Try again!")  # –û–±—Ä–æ–±–∫–∞ –ø–æ–º–∏–ª–∫–∏ –≤–≤–µ–¥–µ–Ω–Ω—è

# –§—É–Ω–∫—Ü—ñ—è –¥–ª—è —Ä–æ–±–æ—Ç–∏ –≤ –Ω–µ—ñ–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω–æ–º—É —Ä–µ–∂–∏–º—ñ (—á–∏—Ç–∞–Ω–Ω—è –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç—ñ–≤ —ñ–∑ —Ñ–∞–π–ª—É)
def file_mode(filename):
    """
    –ß–∏—Ç–∞—î –∫–æ–µ—Ñ—ñ—Ü—ñ—î–Ω—Ç–∏ —Ä—ñ–≤–Ω—è–Ω–Ω—è –∑ —Ñ–∞–π–ª—É —Ç–∞ —Ä–æ–∑–≤'—è–∑—É—î –π–æ–≥–æ

    –ü–∞—Ä–∞–º–µ—Ç—Ä–∏:
    filename (str): –ù–∞–∑–≤–∞ —Ñ–∞–π–ª—É, —â–æ –º—ñ—Å—Ç–∏—Ç—å —Ç—Ä–∏ —á–∏—Å–ª–∞ (Œ± Œ≤ Œ≥)
    """

    try:
        # –í—ñ–¥–∫—Ä–∏–≤–∞—î–º–æ —Ñ–∞–π–ª –¥–ª—è —á–∏—Ç–∞–Ω–Ω—è
        with open(filename, "r") as f:
            line = f.readline().strip()  # –ß–∏—Ç–∞—î–º–æ –ø–µ—Ä—à–∏–π —Ä—è–¥–æ–∫, –≤–∏–¥–∞–ª—è—î–º–æ –∑–∞–π–≤—ñ –ø—Ä–æ–±—ñ–ª–∏
            parts = line.split()  # –†–æ–∑–±–∏–≤–∞—î–º–æ —Ä—è–¥–æ–∫ –Ω–∞ —Ç—Ä–∏ —á–∞—Å—Ç–∏–Ω–∏ (Œ±, Œ≤, Œ≥)
            
            if len(parts) != 3:
                raise ValueError("Invalid file format")  # –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ –∫–æ—Ä–µ–∫—Ç–Ω—ñ—Å—Ç—å —Ñ–æ—Ä–º–∞—Ç—É
            
            # –ü–µ—Ä–µ—Ç–≤–æ—Ä—é—î–º–æ –∑–Ω–∞—á–µ–Ω–Ω—è –≤ —á–∏—Å–ª–∞
            alpha, beta, gamma = map(float, parts)

            # –í–∏–∫–ª–∏–∫–∞—î–º–æ —Ñ—É–Ω–∫—Ü—ñ—é —Ä–æ–∑–≤‚Äô—è–∑–∫—É
            roots = solve_funny_math(alpha, beta, gamma)

            # –í–∏–≤–æ–¥–∏–º–æ —Ä—ñ–≤–Ω—è–Ω–Ω—è
            print(f"Equation is: ({alpha}) x^2 + ({beta}) x + ({gamma}) = 0")

            # –í–∏–≤–æ–¥–∏–º–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∏
            if len(roots) == 2:
                print("üéâ There are 2 fantastic roots!")
                print(f"‚ú® Root 1: {roots[0]:.6g}")
                print(f"‚ú® Root 2: {roots[1]:.6g}")
            elif len(roots) == 1:
                print("‚≠ê Just 1 unique root!")
                print(f"ü¶Ñ Root: {roots[0]:.6g}")
            else:
                print("üí® No real roots this time, sorry!")

    except FileNotFoundError:
        print(f"üö® Error: File '{filename}' not found.")  # –Ø–∫—â–æ —Ñ–∞–π–ª –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ
    except ValueError as e:
        print(f"üö® Error: {e}")  # –Ø–∫—â–æ –≤–∏–Ω–∏–∫–ª–∞ –ø–æ–º–∏–ª–∫–∞ —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è

# –¢–æ—á–∫–∞ –≤—Ö–æ–¥—É –≤ –ø—Ä–æ–≥—Ä–∞–º—É
if __name__ == "__main__":
    """
    –í–∏–∑–Ω–∞—á–∞—î, —è–∫ –∑–∞–ø—É—Å–∫–∞—Ç–∏ –ø—Ä–æ–≥—Ä–∞–º—É: —É —ñ–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω–æ–º—É –∞–±–æ —Ñ–∞–π–ª–æ–≤–æ–º—É —Ä–µ–∂–∏–º—ñ
    """

    # –Ø–∫—â–æ –ø–µ—Ä–µ–¥–∞–Ω–æ –æ–¥–∏–Ω –∞—Ä–≥—É–º–µ–Ω—Ç (–Ω–∞–∑–≤–∞ —Ñ–∞–π–ª—É), –∑–∞–ø—É—Å–∫–∞—î–º–æ —Ñ–∞–π–ª–æ–≤–∏–π —Ä–µ–∂–∏–º
    if len(sys.argv) == 2:
        file_mode(sys.argv[1])
